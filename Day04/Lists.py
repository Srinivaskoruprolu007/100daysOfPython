# List of fruits
fruits = ["Apple", "Banana", "Guava", "Strawberry"]

# Print the first fruit in the list.
print(fruits[0])

# Replace the first fruit with "Mango".
fruits[0] = "Mango"

# Add "Pineapple" to the end of the list.
fruits.append("Pineapple")

# Print the updated list of fruits.
print(fruits)